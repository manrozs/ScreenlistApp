export const generateRecommendations = (
  userGenres: string[],
  userServices: string[],
  userWatchlistIds: number[]
) => {
  // Filter shows based on user preferences
  let recommendations = mockShows.filter(show => {
    // Don't recommend shows that are already in the watchlist
    if (userWatchlistIds.includes(show.id)) return false;
    
    // Check if the show is on one of the user's streaming services
    const serviceMatch = userServices.length === 0 || userServices.includes(show.streamingService);
    
    // Check if the show matches at least one of the user's genres
    const genreMatch = userGenres.length === 0 || show.genres.some(genre => userGenres.includes(genre));
    
    return serviceMatch && genreMatch;
  });
  
  // Sort by rating (descending)
  recommendations.sort((a, b) => parseFloat(b.rating) - parseFloat(a.rating));
  
  // Return top recommendations
  return recommendations.slice(0, 8);
};
